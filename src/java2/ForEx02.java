package java2;

public class ForEx02 {

	public static void main(String[] args) {
		
		for (int i = 1; i < 10; i++) {
			for (int j = 2; j < 10; j++) {
				System.out.print(j + " * " + i + " = " + (j * i) + "\t\t");
				//System.out.println(n1+" * " + i + " = "+ (n1 *i));
			}
			System.out.println();
		}	

	}

}
